
# sequential Control Structure: run code top to bottom, without revisiting previous line of code

"""
1. collect the length and store in a variable
2. collect the breadth and store in a variable
3. calculate the area by multiplying length and breadth
4. store the result in a variable called area
5. print the value
"""

# input statements
length = float(input("Enter the length of the rectangle: "))

breadth = float(input("Enter the breadth of the rectangle: "))

# processing
area = length * breadth


# output
print(f"The area of a rectangle is {area}")

